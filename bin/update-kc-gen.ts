import * as fs from "fs/promises";
import { join as pathJoin } from "path";
import type { BuildContext } from "./core";

export async function command(params: { buildContext: BuildContext }) {
    const { buildContext } = params;

    const themeNames = buildContext.themeNames;
    const themeNamesUnion = themeNames.map(n => `"${n}"`).join(" | ");
    const themeNamesArray = themeNames.map(n => `"${n}"`).join(", ");

    const kcGenTs = [
        `// This file is auto-generated by keycloakify (Vue PoC handler). Do not edit it manually.`,
        ``,
        `/* eslint-disable */`,
        `// @ts-nocheck`,
        ``,
        `export type ThemeName = ${themeNamesUnion};`,
        ``,
        `export const themeNames: ThemeName[] = [${themeNamesArray}];`,
        ``,
        `export type KcEnvName = never;`,
        `export const kcEnvNames: KcEnvName[] = [];`,
        `export const kcEnvDefaults: Record<KcEnvName, string> = {};`,
        ``,
        `export type KcContext = import("./login/KcContext").KcContext;`,
        ``,
        `declare global {`,
        `    interface Window {`,
        `        kcContext?: KcContext;`,
        `    }`,
        `}`,
        ``
    ].join("\n");

    await fs.writeFile(pathJoin(buildContext.themeSrcDirPath, "kc.gen.ts"), kcGenTs);
}
