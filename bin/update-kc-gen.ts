import * as fs from "fs/promises";
import { existsSync } from "fs";
import { join as pathJoin } from "path";
import type { BuildContext } from "./core";

export async function command(params: { buildContext: BuildContext }) {
    const { buildContext } = params;

    const hasLoginTheme = buildContext.implementedThemeTypes.login.isImplemented;
    const hasAccountTheme = buildContext.implementedThemeTypes.account.isImplemented;

    const themeNames = buildContext.themeNames;
    const themeNamesUnion = themeNames.map(n => `"${n}"`).join(" | ");
    const themeNamesArray = themeNames.map(n => `"${n}"`).join(", ");

    const kcContextMembers = [
        hasLoginTheme && `    | import("./login/KcContext").KcContext`,
        hasAccountTheme && `    | import("./account/KcContext").KcContext`
    ].filter((x): x is string => typeof x === "string");

    const kcGenTs = [
        `// This file is auto-generated by keycloakify. Do not edit it manually.`,
        ``,
        `/* eslint-disable */`,
        `// @ts-nocheck`,
        ``,
        `export type ThemeName = ${themeNamesUnion};`,
        ``,
        `export const themeNames: ThemeName[] = [${themeNamesArray}];`,
        ``,
        `export type KcEnvName = never;`,
        `export const kcEnvNames: KcEnvName[] = [];`,
        `export const kcEnvDefaults: Record<KcEnvName, string> = {};`,
        ``,
        `export type KcContext =`,
        ...kcContextMembers,
        ``,
        `declare global {`,
        `    interface Window {`,
        `        kcContext?: KcContext;`,
        `    }`,
        `}`,
        ``
    ].join("\n");

    const kcGenVue = [
        `<script setup lang="ts">`,
        `// This file is auto-generated by keycloakify. Do not edit it manually.`,
        `import { defineAsyncComponent } from "vue";`,
        `import type { KcContext } from "./kc.gen";`,
        ``,
        `const props = defineProps<{ kcContext: KcContext }>();`,
        ``,
        hasLoginTheme && `const KcLoginPage = defineAsyncComponent(() => import("./login/KcPage.vue"));`,
        hasAccountTheme && `const KcAccountPage = defineAsyncComponent(() => import("./account/KcPage.vue"));`,
        `</script>`,
        ``,
        `<template>`,
        hasLoginTheme && `    <KcLoginPage v-if="props.kcContext.themeType === 'login'" :kcContext="props.kcContext" />`,
        hasAccountTheme && `    <KcAccountPage v-else-if="props.kcContext.themeType === 'account'" :kcContext="props.kcContext" />`,
        `</template>`,
        ``
    ]
        .filter((x): x is string => typeof x === "string")
        .join("\n");

    await fs.writeFile(pathJoin(buildContext.themeSrcDirPath, "kc.gen.ts"), kcGenTs);

    const kcGenVuePath = pathJoin(buildContext.themeSrcDirPath, "kc.gen.vue");
    if (existsSync(kcGenVuePath) || hasLoginTheme || hasAccountTheme) {
        await fs.writeFile(kcGenVuePath, kcGenVue);
    }
}
